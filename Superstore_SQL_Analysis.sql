-- Find Top 5 Customers by Profit
-- Requirements: customer_name, total_sales, total_profit, profit_rank
-- Use: GROUP BY RANK()
-- A manager would care because:
	-- These customers contribute significantly to company profitability and 
	-- could be targeted for retention, loyalty programs, and premium offerings.
-- Why did you use RANK() instead of ORDER BY profit DESC LIMIT 5?
	-- ORDER BY and LIMIT can show the top 5 records, but RANK() explicitly provides the ranking position 
	-- and handles ties in a meaningful way. It also makes the query easier to extend for Top N analyses.
SELECT
    customer_name,
    ROUND(SUM(sales),2) AS total_sales,
	ROUND(SUM(profit),2) AS total_profit,
    RANK() OVER (ORDER BY SUM(profit) DESC) AS profit_rank
FROM superstore
GROUP BY customer_name
ORDER BY profit_rank
LIMIT 5;

