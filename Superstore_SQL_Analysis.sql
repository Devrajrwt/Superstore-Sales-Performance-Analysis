-- CTEs (Common Table Expressions)
-- Let's find categories with profit above the average category profit.
-- Why use a CTE instead of a subquery?
	-- CTEs improve readability and make complex queries easier to understand and maintain. 
	-- They also allow intermediate results to be reused within the same query.
-- Which categories were above average profit?
	-- "Office Supplies"	122490.88
	-- "Technology"     	145455.66
-- In your own words, what did the CTE do?
	-- It helped me find categories with profit above the average category profit.
WITH category_profit AS (
    SELECT
        category,
        SUM(profit) AS total_profit
    FROM superstore
    GROUP BY category
)
SELECT *
FROM category_profit
WHERE total_profit >
(
    SELECT AVG(total_profit)
    FROM category_profit
);

-- RANK() 
	-- Gaps exist.
	-- Who is ranked #1?
		-- Customer_name  "Sean Miller",  total_sales 25043.07,  Sales_Rank 1
	-- Are there any ties in the top 10?
		-- No
	-- In your own words, what is the difference between GROUP BY and RANK()?
		-- GROUP BY takes many rows and aggregates them.
		-- GROUP BY reduces many rows into summary rows.
			-- Final, GROUP BY aggregates rows into groups and calculates summary metrics such as total sales.
		-- RANK doesn't reduce rows. It takes the summarized result and adds a ranking.
		-- RANK() adds analytical information without changing the number of rows.
			-- Final, RANK is a window function that assigns rankings to the resulting rows based on a specified 
			-- order without further aggregating the data.
SELECT
    customer_name,
    ROUND(SUM(sales),2) AS total_sales,
    RANK() OVER (ORDER BY SUM(sales) DESC) AS sales_rank
FROM superstore
GROUP BY customer_name
ORDER BY sales_rank
LIMIT 10;

-- DENSE_RANK() 
	-- No gaps.
SELECT
    customer_name,
    ROUND(SUM(sales),2) AS total_sales,
    DENSE_RANK() OVER (ORDER BY SUM(sales) DESC) AS sales_rank
FROM superstore
GROUP BY customer_name
ORDER BY sales_rank
LIMIT 10;

-- ROW_NUMBER()
-- Did the top 10 results change?
	-- No
-- In what business scenario would you use:
	-- RANK(): Use RANK() when ties should receive the same rank.
	-- ROW_NUMBER(): Use ROW_NUMBER() when every row must have a unique position, even if values are tied.
SELECT
    customer_name,
    ROUND(SUM(sales),2) AS total_sales,
    ROW_NUMBER() OVER (ORDER BY SUM(sales) DESC) AS row_num
FROM superstore
GROUP BY customer_name
ORDER BY row_num
LIMIT 10;

-- ROW_NUMBER()
	-- If A and B have exactly the same sales, who gets 1 and who gets 2?
	-- PostgreSQL chooses based on the ordering available, 
	-- and if the ORDER BY values are tied, the result can be arbitrary/non-deterministic unless you add another sorting column.
-- when many customers have the same sales.
-- because now the ranking is deterministic and repeatable.
-- A better approach is:
SELECT
    customer_name,
    ROUND(SUM(sales),2) AS total_sales,
    ROW_NUMBER() OVER (ORDER BY SUM(sales) DESC, customer_name) AS row_num
FROM superstore
GROUP BY customer_name
ORDER BY row_num
LIMIT 10;


